package com.example.btl.object;

public class class_timeKeeping {
    String idE, name, check;

    public class_timeKeeping(String idE, String name, String check) {
        this.idE = idE;
        this.name = name;
        this.check = check;
    }

    public String getIdE() {
        return idE;
    }

    public String getName() {
        return name;
    }

    public String getCheck() {
        return check;
    }
}
